import UIKit

// MARK: For
// Help us for iterinate from collection items.
for numero in 1...4 {
    print(numero)
}

//for de un Array
var paises = ["ARG","EUA","JP"]
for pais in paises {
    print(pais)
}

//for de un dictionary
var paisesDictionary = ["ARG":"Argentina","EUA":"Estados Unidos","JP":"Japon"]
for (key,pais) in paisesDictionary {
    print("\(key) -- \(pais)")
}


